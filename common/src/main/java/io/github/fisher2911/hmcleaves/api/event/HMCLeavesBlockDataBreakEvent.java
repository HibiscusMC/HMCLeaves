package io.github.fisher2911.hmcleaves.api.event;

import io.github.fisher2911.hmcleaves.data.BlockData;
import org.bukkit.block.Block;
import org.bukkit.entity.Player;
import org.bukkit.event.block.BlockBreakEvent;
import org.jetbrains.annotations.NotNull;

public class HMCLeavesBlockDataBreakEvent extends BlockBreakEvent {

    private final BlockData blockData;

    public HMCLeavesBlockDataBreakEvent(@NotNull Block theBlock, @NotNull Player player, BlockData blockData) {
        super(theBlock, player);
        this.blockData = blockData;
    }

    public BlockData getBlockData() {
        return this.blockData;
    }

}
